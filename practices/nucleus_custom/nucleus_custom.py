class NucleusCustomDataset(utils.Dataset):

    def load_nucleus_custom(self, dataset_dir, subset):
        """Load a subset of the nuclei dataset.

        dataset_dir: Root directory of the dataset
        subset: Subset to load. Either the name of the sub-directory,
                such as stage1_train, stage1_test, ...etc. or, one of:
                * train: stage1_train excluding validation images
                * val: validation images from VAL_IMAGE_IDS
        """
        # Add classes. We have one class.
        # Naming the dataset nucleus, and the class nucleus
        self.add_class(source="nucleus_custom", class_id=1, class_name="nucleus")

        # Which subset?
        assert subset in ["train", "val", "test"]
        subset_dir = subset
        dataset_dir = os.path.join(dataset_dir, subset_dir)
        
        # Get image ids from directory names
        image_ids = next(os.walk(dataset_dir))[1]

        # Add images
        for image_id in image_ids:
            self.add_image(
                "nucleus_custom",
                image_id=image_id,
                path=os.path.join(dataset_dir, image_id, "images/{}.tif".format(image_id)))

    def load_mask(self, image_id):
        """Generate instance masks for an image.
       Returns:
        masks: A bool array of shape [height, width, instance count] with
            one mask per instance.
        class_ids: a 1D array of class IDs of the instance masks.
        """
        info = self.image_info[image_id]
        # Get mask directory from image path
        mask_dir = os.path.join(os.path.dirname(os.path.dirname(info['path'])), "masks")

        # Read mask files from .png image
        mask = []
        for f in next(os.walk(mask_dir))[2]:
            if f.endswith(".png"):
                m = skimage.io.imread(os.path.join(mask_dir, f)).astype(np.bool)
                mask.append(m)
        mask = np.stack(mask, axis=-1)
        # Return mask, and array of class IDs of each instance. Since we have
        # one class ID, we return an array of ones
        return mask, np.ones([mask.shape[-1]], dtype=np.int32)

    def image_reference(self, image_id):
        """Return the path of the image."""
        info = self.image_info[image_id]
        if info["source"] == "nucleus_custom":
            return info["id"]
        else:
            super(self.__class__, self).image_reference(image_id)


